import UIKit

extension UIColor {
    static var main: UIColor {
        return UIColor.myPurple
    }
    
}

extension UIColor {
    static var myPurple: UIColor {
        return UIColor(red: 119.0 / 255.0, green: 31.0 / 255.0, blue: 134.0 / 255.0, alpha: 1.0)
    }
}
