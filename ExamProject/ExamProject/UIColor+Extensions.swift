import UIKit

extension UIColor {
    static var main: UIColor {
        return UIColor.myPurple
    }
    
}

extension UIColor {
    static var myRedPurple: UIColor {
        return UIColor(red: 33.0 / 255.0, green: 11.0 / 255.0, blue: 44.0 / 255.0, alpha: 1.0)
    }
    
    static var myPurple: UIColor {
        return UIColor(red: 119.0 / 255.0, green: 31.0 / 255.0, blue: 134.0 / 255.0, alpha: 1.0)
    }
    static var myBrightLavender: UIColor {
        return UIColor(red: 188.0 / 255.0, green: 150.0 / 255.0, blue: 230.0 / 255.0, alpha: 1.0)
    }
    static var myPinkLavender: UIColor {
        return UIColor(red: 216.0 / 255.0, green: 180.0 / 255.0, blue: 226.0 / 255.0, alpha: 1.0)
    }
    static var myPearlyPurple: UIColor {
        return UIColor(red: 174.0 / 255.0, green: 117.0 / 255.0, blue: 159.0 / 255.0, alpha: 1.0)
    }
}
